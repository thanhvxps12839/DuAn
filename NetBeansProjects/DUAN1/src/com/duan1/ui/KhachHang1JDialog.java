    void edit() {
        int Makhachhang = (int) tbl_khachhang.getValueAt(this.row, 0);
        KhachHang kh = dao.selectById(Makhachhang);
        this.setForm(kh);

    }

    void setForm(KhachHang kh) {
        txt_makh.setText(String.valueOf(kh.getMakhachhang()));
        txt_tenkh.setText(kh.getTenkhachhang());
        txt_ngaysinh.setText(XDate.toString(kh.getNgaysinh(), "yyyy/MM/dd"));
        txt_diachi.setText(kh.getDiachi());
        txt_dienthoai.setText(kh.getDienthoai());
    }

    KhachHang getForm() {
        KhachHang kh = new KhachHang();
        kh.setMakhachhang(Integer.valueOf(txt_makh.getText()));
        kh.setTenkhachhang(txt_tenkh.getText());
        kh.setNgaysinh(XDate.toDate(txt_ngaysinh.getText(), "yyyy/MM/dd"));
        kh.setDiachi(txt_diachi.getText());
        kh.setDienthoai(txt_dienthoai.getText());

        return kh;
    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void last() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tbl_khachhang.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void prev() {
        this.row = tbl_khachhang.getRowCount() - 1;
        this.edit();
    }

