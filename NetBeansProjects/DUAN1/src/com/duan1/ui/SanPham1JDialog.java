    String chuyenTien(double amount) {
        //String newname  = amount.subString(0,amount.length()-1);
        Locale locale = new Locale("vi", "VN");
        NumberFormat currencyFormatter = NumberFormat.getCurrencyInstance(locale);

        return currencyFormatter.format(amount);

    }

    void first() {
        this.row = 0;
        this.edit();
    }

    void last() {
        if (this.row > 0) {
            this.row--;
            this.edit();
        }
    }

    void next() {
        if (this.row < tbl_sanpham.getRowCount() - 1) {
            this.row++;
            this.edit();
        }
    }

    void prev() {
        this.row = tbl_sanpham.getRowCount() - 1;
        this.edit();
    }

