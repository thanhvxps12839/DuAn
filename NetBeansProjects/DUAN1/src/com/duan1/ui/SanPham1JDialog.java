    void init() {
        setLocationRelativeTo(null);
        this.fillTable();
        this.row = -1;

    }

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tbl_sanpham.getModel();
        model.setRowCount(0);
        try {
            List<SanPham> list = dao.selectAll();
            for (SanPham sp : list) {
                Object[] row = {
                    sp.getMasanpham(),
                    sp.getTensanpham(),
                    sp.getMahangdan(),
                    sp.getSoluong(),
                    formatter.format(sp.getDongianhap()),
                    formatter.format(sp.getDongiaban()),
                    sp.getAnh(),
                    sp.getGhichu()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!" + e);
        }
    }

    void insert() {
        SanPham model = getForm();
        try {
            dao.insert(model);
            this.fillTable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!" + e);
        }

    }

    void update() {
        SanPham nv = getForm();
        try {
            dao.update(nv);
            this.fillTable();
            MsgBox.alert(this, "Cập nhật thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Cập nhật thất bại!" + e.toString());
        }
    }

    void delete() {
        if (!Auth.isManager()) {
            MsgBox.alert(this, "Bạn không có quyền xóa sản phẩm!");
        } else {
            if (MsgBox.confirm(this, "Bạn có muốn xóa hay không?")) {
                String masp = txt_masp.getText();
                try {
                    System.out.println(masp);
                    dao.delete(masp);

                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!" + e);
                }
            }
        }
    }

    void clearForm() {
        this.setForm(new SanPham());

        this.row = -1;

    }

    void setForm(SanPham sp) {

        txt_masp.setText(String.valueOf(sp.getMasanpham()));
        txt_tensp.setText(sp.getTensanpham());
        txt_mahangdan.setText(String.valueOf(sp.getMahangdan()));
        txt_soluong.setText(String.valueOf(sp.getSoluong()));
       // txt_dongianhap.setText(String.valueOf(chuyenTien(sp.getDongianhap())));
       txt_dongianhap.setText(formatter2.format(sp.getDongianhap()));
        txt_dongiaban.setText(formatter2.format(sp.getDongiaban()));
        if (sp.getAnh() != null) {
            jlb_anh.setToolTipText(sp.getAnh());
            jlb_anh.setIcon(XImage.read(sp.getAnh()));

        }
        tar_ghichu.setText(sp.getGhichu());
    }

    SanPham getForm() {
        SanPham sp = new SanPham();
        sp.setMasanpham(Integer.valueOf(txt_masp.getText()));
        sp.setTensanpham(txt_tensp.getText());
        sp.setMahangdan(Integer.valueOf(txt_mahangdan.getText()));
        sp.setSoluong(Double.valueOf(txt_soluong.getText()));
        sp.setDongianhap(Double.valueOf(txt_dongianhap.getText()));
        sp.setDongiaban(Double.valueOf(txt_dongiaban.getText()));
        sp.setAnh(jlb_anh.getToolTipText());
        sp.setGhichu(tar_ghichu.getText());

        return sp;
    }

    void edit() {
        int Masanpham = (int) tbl_sanpham.getValueAt(this.row, 0);
        SanPham sp = dao.selectById(Masanpham);
        this.setForm(sp);

    }

